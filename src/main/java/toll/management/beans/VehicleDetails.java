package toll.management.beans;

import java.util.HashMap;
import java.util.Map;

public class VehicleDetails {

	private String id;
	private String vehicleNumber;
	private String expiryDate;
	private String isValid;
	private String singleWay;
	private String returnJourney;
	private String amount;
	private String route;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getVehicleNumber() {
		return vehicleNumber;
	}
	public void setVehicleNumber(String vehicleNumber) {
		this.vehicleNumber = vehicleNumber;
	}
	public String getExpiryDate() {
		return expiryDate;
	}
	public void setExpiryDate(String expiryDate) {
		this.expiryDate = expiryDate;
	}
	public String getIsValid() {
		return isValid;
	}
	public void setIsValid(String isValid) {
		this.isValid = isValid;
	}
	public String getSingleWay() {
		return singleWay;
	}
	public void setSingleWay(String singleWay) {
		this.singleWay = singleWay;
	}
	public String getReturnJourney() {
		return returnJourney;
	}
	public void setReturnJourney(String returnJourney) {
		this.returnJourney = returnJourney;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getRoute() {
		return route;
	}
	public void setRoute(String route) {
		this.route = route;
	}
	
	
	
}
